// for
for (let i = 1; i <= 5; i++) {
  console.log("Número: " + i);
}

// No exemplo acima, o loop for inicia i com o valor 1, e executa o bloco enquanto
// i for menor ou igual a 5. O resultado é a impressão dos nímeros de 1 a 5 no console.

// while
let contador = 5;

while (contador > 0) {
  console.log("Contagem: " + contador);
  contador--;
}

// No exemplo acima, o loop while inicia com a variável contador igual a 5, e executa
// o bloco enquanto o contador for maior que 0. O resultado é a impressão da contagem de 5 a 1 no console.